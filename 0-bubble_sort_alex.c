#include "sort.h"
/**
 * bubble_sort - bubble sorting function
 * Return: void
 * @array: int ptr
 * @size: size_t
 * Description: Bubble sorting function
 */
void bubble_sort(int *array, size_t size)
{
	size_t i, j;

	if (!array || size < 2)
		return;

	for (i = 0; i < size - 1; i++)
		for (j = 0; j < size - i - 1; j++)
			if (array[j] > array[j + 1])
			{
				swap(&array[j], &array[j + 1]);
				print_array(array, size);
			}
}
